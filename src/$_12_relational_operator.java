public class $_12_relational_operator {
    public static void main(String[] args) {
        boolean b=2<3;
        System.out.println(b);
        boolean a=2<=3;
        System.out.println(a);
        boolean c=5>3;
        System.out.println(c);
        boolean d=5>=4;
        System.out.println(d);
        boolean e=5==5;
        System.out.println(e);
        boolean f=5!=5;
        System.out.println(f);
    }
}
