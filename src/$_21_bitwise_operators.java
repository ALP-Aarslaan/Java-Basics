public class $_21_bitwise_operators {
    public static void main(String[] args) {
        int a=32;
        int b=12;
        int c=a&b;
        System.out.println(c);
        c=a|b;
        System.out.println(c);
        c=a^b;
        System.out.println(c);
        c=a>>2;
        System.out.println(c);
        c=a<<3;
        System.out.println(c);
    }
}
